/*
	WEEKOFYEAR() --> it returns the week of the year
    
    WEEK() --> it is its synonym
*/


SELECT WEEKOFYEAR('2023-01-05');
SELECT WEEKOFYEAR('2023-02-10');
SELECT WEEKOFYEAR('2023-01-10');
SELECT WEEKOFYEAR('2023-12-31');


SELECT WEEK('2023-01-05');
SELECT WEEK('2023-02-10');
SELECT WEEK('2023-01-10');
SELECT WEEK('2023-12-31');