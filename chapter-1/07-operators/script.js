// 3 - comparision operators
console.log("2 == 2 --> ", 2 == 2);
console.log("2 == 4 --> ",2 == 4);
console.log("2 != 2 --> ",2 != 2);
console.log("2 != 4 --> ",2 != 4);
console.log("2 > 2 --> ",2 > 2);
console.log("2 >= 2 --> ",2 >= 2);
console.log("2 < 2 --> ",2 < 2);
console.log("2 <= 2 --> ", 2 <= 2);

// remember "==" only evaluates the value but "===" evaluates the type also
console.log("2 == 2 --> ",2 == 2);
console.log("2 == '2' --> ",2 == '2');
console.log("2 === 2 --> ",2 === 2);
console.log("2 === '2' --> ",2 === '2');
// similarly
console.log("2 != '2' --> ",2 != '2');
console.log("2 !== '2' --> ",2 !== '2');



